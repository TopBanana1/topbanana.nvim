vim.keymap.set("n", "<leader><leader>", function()
    for name, _ in pairs(package.loaded) do
        if name:match("^user") or name:match("^plugins") or name:match("^config") then
            package.loaded[name] = nil
        end
    end

    vim.cmd("source $MYVIMRC")

    require("lazy").reload()
end, { silent = true })

